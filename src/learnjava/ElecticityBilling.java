    private void calBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calBtnActionPerformed
        
        Cusid = t1.getText();
        count = Integer.parseInt(t3.getText());
        
        if (count < 500)
        {
            charge = 1.00;
        }
        else if (count < 500 && count < 600)
        {
            charge = 1.00;
        }
        else if (count < 600 && count < 800)
        {
            charge = 2.80;
        }
        else 
        {
            charge = 3.00;
        }
        
        gramount = charge * count;
        
        print();
        
        
        
    }//GEN-LAST:event_calBtnActionPerformed

